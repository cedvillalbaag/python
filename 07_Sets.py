#Sets  - Colección desordenada que no tiene un índice

#Cuando no se requiere ordenar el conjunto de datos

#Definir set
colors = {"red","green","blue"}

#ver metodos y propiedades
print(type(colors))

#Imprimir set
print(colors)

#Buscar el elemento en el set
print("red" in colors)

#Add - Agregar elemento al Set
colors.add("orange")

# Remove - Remover elemento del set
colors.remove("orange")

#Clear - Eliminar todos los elementos del set
colors.clear()

#Eliminar set
del colors

